// Project gallery

var showMore1 = document.querySelector('.button-1');
var photoOverlay1 = document.querySelector('.overlay-1');
var projDescription1 = document.getElementById('gallery-item-1');
var arrow1 = document.querySelector('.arrow-1');

showMore1.addEventListener('click', function() {
	showMore1.style.display = 'none';
	photoOverlay1.style.opacity = 0;
	projDescription1.style.display = 'flex';
});
arrow1.addEventListener('click', function() {
	showMore1.style.display = 'block';
	photoOverlay1.style.opacity = 0.8;
	projDescription1.style.display = 'none';
});

